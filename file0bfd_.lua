RotationTrigger01={}



function RotationTrigger01:Init()

end



function RotationTrigger01:Update()

	-----------------------------------------------------------------------------------------
	--Si le pod du joueur entre dans le trigger
	-----------------------------------------------------------------------------------------
	if (EnterTrigger("Pod", "CURRENT") ~= nil) then

		-----------------------------------------------------------------------------------------
		--L'evenement est démarré
		-----------------------------------------------------------------------------------------
		StartEvent( "rototo" )

	end

	-----------------------------------------------------------------------------------------
	--Si le pod du joueur quitte le trigger
	-----------------------------------------------------------------------------------------
	if (LeaveTrigger("Pod", "CURRENT") ~= nil) then

		-----------------------------------------------------------------------------------------
		--L'evenement est stoppé
		-----------------------------------------------------------------------------------------
		StopEvent( "rototo" )

	end

end



function RotationTrigger01:End()

end
